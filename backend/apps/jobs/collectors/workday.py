import requests

from apps.jobs.collectors.base import (
    BaseCollector,
)

from apps.jobs.services.normalization_service import (
    JobNormalizationService,
)


class WorkdayCollector(
    BaseCollector
):

    source_name = "Workday"

    base_url = "https://myworkdayjobs.com"

    def fetch_jobs(
        self
    ):

        all_jobs = []

        for company in WORKDAY_COMPANIES:

            try:

                jobs = (
                    self.fetch_company_jobs(
                        company
                    )
                )

                all_jobs.extend(
                    jobs
                )

            except Exception as e:

                print(
                    f"{company['name']} failed: {e}"
                )

        return all_jobs

    def fetch_company_jobs(
        self,
        company
    ):

        return []

    def normalize(
        self,
        raw_job
    ):

        return (
            JobNormalizationService
            .normalize_workday(
                raw_job
            )
        )